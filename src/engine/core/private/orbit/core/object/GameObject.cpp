        // CONSTRUCTOR & DESTRUCTOR
        // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

        GameObject::GameObject()
            :
            Entity(),
            mLoaded(false)
        {
        }

        GameObject::~GameObject() noexcept
        {
            Unload();
        }

        // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        // GETTERS & SETTERS
        // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

        bool GameObject::isLoaded() const noexcept
        {
            return mLoaded;
        }

        // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        // METHODS.ILoadable
        // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

        bool GameObject::Load()
        {
            if (isLoaded())
                return true;

            return true;
        }

        void GameObject::Unload()
        {
            if (!isLoaded())
                return;
        }

